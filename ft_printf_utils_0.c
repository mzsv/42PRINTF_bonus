#include "ft_printf.h"

void	ft_putnstr_fd(char *s, size_t n, int fd)
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] && i < n)
	{
		write(fd, (s + i), 1);
		i++;
	}
}
